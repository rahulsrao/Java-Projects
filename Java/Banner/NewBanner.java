    private void NewBanner(){
        
        new Thread(){
            
            @Override
            public void run(){
                String text="          My new banner";
                char ch=' ';
                while(true){
                    ch=text.charAt(0);
                    text=text.substring(1,text.length());
                    banner.setText(text+=ch);
                    
                    try{
                        Thread.sleep(350);
                    }
                    catch(InterruptedException e){ }
                }
            }
            
        }.start();
    }
    
